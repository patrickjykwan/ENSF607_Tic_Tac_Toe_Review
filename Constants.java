package exercise3;

/**
 * This interface class Constants defines the play pieces for the game of tic-tac-toe. That would be the
 * SPACE_CHAR which is an empty space holder, LETTER_O which is a player piece and LETTER_x which is another player piece.
 *
 * @author Patrick Kwan
 * @version 1.0
 * @since September 28 2020
 */
public interface Constants {

	static final char SPACE_CHAR = ' ';
	static final char LETTER_O = 'O';
	static final char LETTER_X = 'X';
}
